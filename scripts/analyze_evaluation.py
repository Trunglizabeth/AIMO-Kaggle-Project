"""Analyze an evaluation report and print summary metrics.

This script reads a CSV report generated by `scripts/evaluate.py` and prints
accuracy, error counts, top error categories, and example failures.
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path
import pandas as pd


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description='Analyze evaluation reports for AIMO solver.')
    parser.add_argument('--report', default='data/evaluation_report.csv', help='Input evaluation report CSV.')
    parser.add_argument('--top-errors', type=int, default=10, help='Number of top error messages to print.')
    parser.add_argument('--output-summary', default=None, help='Optional path to save JSON summary.')
    parser.add_argument('--output-markdown', default=None, help='Optional path to save Markdown summary.')
    parser.add_argument('--top-wrong', type=int, default=5, help='Number of top wrong examples to show.')
    return parser.parse_args()


def categorize_error(error: str) -> str:
    if not error or pd.isna(error):
        return 'No error'
    error = str(error).strip()
    if 'Timeout' in error:
        return 'Timeout'
    if 'No code extracted' in error:
        return 'No code extracted'
    if 'Parsing predicted answer failed' in error:
        return 'Parsing failure'
    if error.startswith('Error'):
        return 'Execution error'
    if 'Mock fallback' in error:
        return 'Mock fallback'
    return 'Other error'


def format_markdown_summary(stats: dict[str, object]) -> str:
    lines = [
        '# Evaluation Summary',
        f'- Total examples: {stats["total"]}',
        f'- Correct: {stats["correct"]}',
        f'- Incorrect: {stats["incorrect"]}',
        f'- Accuracy: {stats["accuracy"]:.2%}',
        f'- Missing predictions: {stats["missing_predictions"]}',
        f'- Timeout count: {stats["timeout_count"]}',
        '',
        '## Error categories',
    ]
    for category, count in stats['error_categories'].items():
        lines.append(f'- {category}: {count}')

    lines.append('')
    lines.append('## Prediction methods')
    for method, count in stats['prediction_methods'].items():
        lines.append(f'- {method}: {count}')

    lines.append('')
    lines.append('## Top error messages')
    for error, count in stats['error_counts'].items():
        if error:
            lines.append(f'- {count} × {error}')
        else:
            lines.append(f'- {count} × No error')

    if stats['top_wrong_examples']:
        lines.append('')
        lines.append('## Top wrong examples')
        for example in stats['top_wrong_examples']:
            lines.append(f'- **id**: {example["id"]}, **true**: {example["true_answer"]}, **predicted**: {example["predicted_answer"]}, **error**: {example["error_log"]}')

    return '\n'.join(lines)


def save_markdown_summary(stats: dict[str, object], output_path: Path) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(format_markdown_summary(stats) + '\n', encoding='utf-8')


def summarize_report(report_path: Path, top_errors: int = 10, top_wrong: int = 5) -> dict[str, object]:
    if not report_path.exists():
        raise FileNotFoundError(f'Không tìm thấy báo cáo: {report_path}')

    df = pd.read_csv(report_path)
    total = len(df)
    correct = int(df['is_correct'].sum()) if 'is_correct' in df.columns else 0
    incorrect = total - correct
    accuracy = correct / total if total > 0 else 0.0

    missing = int(df['predicted_answer'].isna().sum()) if 'predicted_answer' in df.columns else 0
    timeout_count = int(df['error_log'].str.contains('Timeout', na=False).sum()) if 'error_log' in df.columns else 0
    error_counts = df['error_log'].fillna('No error').replace('', 'No error').value_counts().head(top_errors).to_dict() if 'error_log' in df.columns else {}
    error_categories = df['error_log'].fillna('').map(categorize_error).value_counts().to_dict() if 'error_log' in df.columns else {}
    prediction_methods = df['prediction_method'].fillna('unknown').value_counts().to_dict() if 'prediction_method' in df.columns else {}

    wrong_examples = []
    if 'predicted_answer' in df.columns and 'true_answer' in df.columns:
        wrong_df = df[df['is_correct'] == False].head(top_wrong)
        for _, row in wrong_df.iterrows():
            wrong_examples.append({
                'id': row.get('id'),
                'problem': row.get('problem'),
                'true_answer': row.get('true_answer'),
                'predicted_answer': row.get('predicted_answer'),
                'error_log': row.get('error_log'),
            })

    return {
        'total': total,
        'correct': correct,
        'incorrect': incorrect,
        'accuracy': accuracy,
        'missing_predictions': missing,
        'timeout_count': timeout_count,
        'error_counts': error_counts,
        'error_categories': error_categories,
        'prediction_methods': prediction_methods,
        'top_wrong_examples': wrong_examples,
    }


def main() -> None:
    args = parse_args()
    report_path = Path(args.report)
    stats = summarize_report(report_path, top_errors=args.top_errors, top_wrong=args.top_wrong)

    print(f'Báo cáo đánh giá: {report_path}')
    print(f'  Tổng số mẫu: {stats["total"]}')
    print(f'  Đúng: {stats["correct"]}')
    print(f'  Sai: {stats["incorrect"]}')
    print(f'  Độ chính xác: {stats["accuracy"]:.2%}')
    print(f'  Số mẫu không có dự đoán: {stats["missing_predictions"]}')
    print(f'  Số lỗi timeout: {stats["timeout_count"]}')
    print('  Top lỗi:')
    for error, count in stats['error_counts'].items():
        display_error = error if error else 'No error'
        print(f'    {count} × {display_error}')
    print('  Phân loại lỗi:')
    for category, count in stats['error_categories'].items():
        print(f'    {count} × {category}')
    if stats.get('prediction_methods'):
        print('  Phương thức dự đoán:')
        for method, count in stats['prediction_methods'].items():
            print(f'    {count} × {method}')
    if stats['top_wrong_examples']:
        print('  Ví dụ sai tiêu biểu:')
        for example in stats['top_wrong_examples']:
            print(f"    - id={example['id']} true={example['true_answer']} pred={example['predicted_answer']} error={example['error_log']}")

    if args.output_summary:
        output_path = Path(args.output_summary)
        output_path.parent.mkdir(parents=True, exist_ok=True)
        with output_path.open('w', encoding='utf-8') as f:
            json.dump(stats, f, ensure_ascii=False, indent=2)
        print(f'  Saved summary to {output_path}')

    if args.output_markdown:
        output_path = Path(args.output_markdown)
        save_markdown_summary(stats, output_path)
        print(f'  Saved markdown summary to {output_path}')


if __name__ == '__main__':
    main()
